    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        // TODO add your handling code here:
        String jtfdid=did.getText();
        String pass=jtfpassword.getText();
        
        RegisterDO reg = register.selectLoginRecord(jtfdid);
        if(jtfdid.equals("")){
            JOptionPane.showMessageDialog(null, "Donor ID field cannot be blank!!","Error!!",JOptionPane.ERROR_MESSAGE);
            
        }
        
        else if(reg!=null){
                if(pass.equals("")){ 
                    JOptionPane.showMessageDialog(null, "Password field cannot be blank!!","Error!!",JOptionPane.ERROR_MESSAGE);
                    
                   
                }else if(!pass.contains(reg.getPassword())){
                    JOptionPane.showMessageDialog(null, "Password incorrect\n Please try again!!","Error!!",JOptionPane.ERROR_MESSAGE);
                    
                }else if(pass.contains(reg.getPassword())){
                    JOptionPane.showMessageDialog(null, "Welcome MR."+reg.getDonorname(),"Welcome",JOptionPane.INFORMATION_MESSAGE);
                    HomePage HM = new HomePage(reg.getDonorid());
                    HM.setVisible(true);
                    this.setVisible(false);
                }
        }else if(jtfdid.length()!=6){
            JOptionPane.showMessageDialog(null, "Donor ID should not be more than or less than 6 character!!\nPlease enter the correct Donor ID.\n For example DR1001", "Invalid Donor ID", JOptionPane.ERROR_MESSAGE);
        }
        else if(reg==null&&jtfdid.length()==6){
            int digit=0;
            int letter=0;
            String chf=jtfdid.substring(2,5);
                for(int i=0;i<chf.length();i++){
                    
                    
                    Character ch = chf.charAt(i);
                    if(Character.isDigit(ch)){
                        digit++;
                    }
                    
                }
                String code=jtfdid.substring(0,2);
                if(code.contains("DR")&&digit==3){
                    JOptionPane.showMessageDialog(null, "No data found!!", "Invalid Donor ID", JOptionPane.ERROR_MESSAGE);
                   
                }
                else if((code.contains("DR"))&&digit!=3){
                    JOptionPane.showMessageDialog(null, "Oops, there was a problem with some of the data you entered. Don't worry let's fix that right now", "Invalid Donor ID", JOptionPane.ERROR_MESSAGE);
                }
        
        }
        
    }//GEN-LAST:event_LoginActionPerformed

